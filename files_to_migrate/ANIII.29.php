<p class=Normal style='margin-left:.75in'> 

<p class=Normal> "Bhikkhus, há três tipos de pessoas que podem ser encontradas no mundo. Quais três? Aquela que é cega, aquela com um olho, e aquela com dois olhos. </p>

<p class=Normal> "E como, bhikkhus,  uma pessoa é cega? Aqui, a uma certa pessoa lhe falta o tipo de olho através do qual ela poderá obter a riqueza que ainda não tenha sido obtida, ou de incrementar a riqueza já obtida, e também lhe falta o tipo de olho através do qual ela poderá distinguir as qualidades prejudiciais das qualidades benéficas, qualidades censuráveis das qualidades isentas de censura, qualidades inferiores das qualidades superiores, qualidades escuras das qualidades luminosas. Essa é chamada a pessoa cega. </p>

<p class=Normal> "E como, bhikkhus,  uma pessoa tem um olho? Aqui, uma certa pessoa tem o tipo de olho através do qual ela poderá obter a riqueza que ainda não tenha sido obtida, ou de incrementar a riqueza já obtida, mas a ela lhe falta o tipo de olho através do qual ela poderá distinguir as qualidades prejudiciais das qualidades benéficas, qualidades censuráveis das qualidades isentas de censura, qualidades inferiores das qualidades superiores, qualidades escuras das qualidades luminosas. Essa é chamada a pessoa que tem um olho. </p>

<p class=Normal> "E como, bhikkhus,  uma pessoa tem dois olhos? Aqui, uma certa pessoa tem o tipo de olho através do qual ela poderá obter a riqueza que ainda não tenha sido obtida, ou de incrementar a riqueza já obtida, e ela também tem o tipo de olho através do qual ela poderá distinguir as qualidades prejudiciais das qualidades benéficas, qualidades censuráveis das qualidades isentas de censura, qualidades inferiores das qualidades superiores, qualidades escuras das qualidades luminosas. Essa é chamada a pessoa que tem dois olhos. </p>

<p class=Normal> "Esses, bhikkhus, são os três tipos de pessoas que podem ser encontradas no mundo." </p>

<p class=Normal> Ele não possui riqueza,<br>
nem realiza méritos; <br>
o homem cego desprovido de olhos<br>
faz uma jogada infeliz em ambos aspectos. </p>

<p class=Normal> O homem com um olho<br>
é um hipócrita que busca a riqueza, <br>
[algumas vezes] com honestidade<br>
[algumas vezes] desonesto. </p>

<p class=Normal> Através de ações fraudulentas
e mentiras<br>
quem desfruta da sensualidade<br>
é hábil em acumular a riqueza. <br>
Tendo partido daqui para o inferno, <br>
o caolho é atormentado. </p>

<p class=Normal> O homem com dois olhos é dito ser<br>
o melhor tipo de pessoa. <br>
A sua riqueza é obtida com honestidade, <br>
através do próprio labor. </p>

<p class=Normal> Com boas intenções ele então dá<br>
com a mente purificada. <br>
Assim segue para o renascimento num excelente estado, <br>
onde tendo ido, ninguém se entristece. </p>

<p class=Normal> Devemos evitar à distância<br>
o cego e o caolho, <br>
mas devemos fraternizar com quem tenha dois olhos, <br>
o melhor tipo de pessoa.</p>

