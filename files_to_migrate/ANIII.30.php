<p class=Normal> "Bhikkhus, há três tipos de pessoas que podem ser encontradas no mundo. Quais três? A pessoa com a sabedoria invertida, a pessoa com a sabedoria como um regaço, e a pessoa com ampla sabedoria. </p>

<p class=Normal>"E como, bhikkhus,  é a pessoa com a sabedoria invertida? Aqui, uma pessoa visita o monastério com frequência para ouvir o Dhamma dos bhikkhus. Os bhikkhus lhe ensinam o Dhamma que é admirável no início, admirável no meio, admirável no final, com o significado e fraseado corretos; e eles revelam uma vida santa que é completamente perfeita e imaculada. Enquanto sentada ela não presta atenção ao que está sendo dito no início, no meio, nem no final. Depois de levantar do seu assento ela ainda não presta atenção ao que foi dito no início, no meio, nem no final. Igual a um jarro que é virado de cabeça para baixo, a água que tenha sido nele colocada escorre e não permanece no jarro, do mesmo modo, uma pessoa visita o monastério com frequência para ouvir o Dhamma dos bhikkhus ... Depois de levantar do seu assento ela ainda não presta atenção ao que foi dito no início, no meio, nem no final. Essa é chamada a pessoa com a sabedoria invertida. </p>

<p class=Normal>"E como, é a pessoa com a sabedoria como um regaço? Aqui, uma pessoa visita o monastério com frequência para ouvir o Dhamma dos bhikkhus. Os bhikkhus lhe ensinam o Dhamma que é admirável no início, admirável no meio, admirável no final, com o significado e fraseado corretos; e eles revelam uma vida santa que é completamente perfeita e imaculada. Enquanto sentada ela presta atenção ao que está sendo dito no início, no meio, nem no final. Mas depois de levantar do seu assento ela não presta atenção ao que foi dito no início, no meio, nem no final. Igual a uma pessoa que tenha uma certa quantidade de gêneros no seu regaço - sementes de sésamo, grãos de arroz, doces, jujubas - se ela não prestar atenção ao se levantar, ela espalha os gêneros por todos os lados, do mesmo modo, uma pessoa visita o monastério com frequência para ouvir o Dhamma dos bhikkhus ... Mas depois de levantar do seu assento ela não presta atenção ao que foi dito no início, no meio, nem no final. Essa é chamada a pessoa com a sabedoria como um regaço. </p>

<p class=Normal>"E como, é a pessoa com ampla sabedoria? Aqui, uma pessoa visita o monastério com frequência para ouvir o Dhamma dos bhikkhus. Os bhikkhus lhe ensinam o Dhamma que é admirável no início, admirável no meio, admirável no final, com o significado e fraseado corretos; e eles revelam uma vida santa que é completamente perfeita e imaculada. Enquanto sentada ela presta atenção ao que está sendo dito no início, no meio, nem no final. Depois de levantar do seu assento novamente ela presta atenção ao que foi dito no início, no meio, nem no final. Igual a um jarro em pé, a água que tenha sido nele colocada permanece e não escorre, do mesmo modo, uma pessoa visita o monastério com frequência para ouvir o Dhamma dos bhikkhus ... Depois de levantar do seu assento novamente ela presta atenção ao que foi dito no início, no meio, nem no final. Essa é chamada a pessoa com a sabedoria invertida. </p>

<p class=Normal>"Esses, bhikkhus, são os três tipos de pessoas que podem ser encontradas no mundo." </p>

<p class=Normal>A pessoa com a sabedoria invertida,<br>
estúpida e sem discernimento, <br>
com frequência visita os bhikkhus<br>
[para ouvir o ensinamento do Dhamma]. </p>

<p class=Normal>Essa pessoa no entanto<br>
não entende nada do que foi dito, <br>
no início, no meio e no final, <br>
pois está completamente desprovida de sabedoria. </p>

<p class=Normal>A pessoa com a sabedoria como um regaço<br>
se diz ser melhor que a primeira. <br>
Ela também com frequência visita os bhikkhus<br>
[para ouvir o ensinamento do Dhamma] </p>

<p class=Normal>Sentada no seu assento, <br>
ela compreende o fraseado do discurso, <br>
no início, no meio e no final. <br>
Mas depois de levantar do seu assento, ela já não mais compreende, <br>
esquece o que aprendeu. </p>

<p class=Normal>A pessoa com ampla sabedoria<br>
é dito ser a melhor de todas. <br>
Ela também com frequência visita os bhikkhus<br>
[para ouvir o ensinamento do Dhamma] </p>

<p class=Normal>Sentada no seu assento, <br>
ela compreende o fraseado do discurso, <br>
no início, no meio e no final. </p>

<p class=Normal>Essa pessoa com a melhor das intenções, <br>
a sua mente purificada, retém aquilo que foi ouvido. <br>
Praticando de acordo com o Dhamma, <br>
ela poderá dar um fim ao sofrimento.</p>

