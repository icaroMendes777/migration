<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>Então, o
Abençoado, tomando um pouco de terra com a ponta da unha, disse aos bhikkhus,
“O que vocês pensam, bhikkhus? O que é maior: a pequena quantidade de terra que
tomei com a ponta da unha ou o grande planeta terra?”<span
style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>



<p class=Normal><a name=R1></a><span lang=PT-BR style='mso-ansi-language:
PT-BR'>“Venerável senhor, o grande planeta terra é muito maior. A pequena
quantidade de terra que o Abençoado tomou com a ponta da unha é quase nada.
Comparado ao grande planeta terra, essa pequena quantidade de terra não é
calculável, não tem comparação, não representa nem uma fração.”<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Assim
também, bhikkhus, aqueles seres que quando falecem como seres humanos renascem
como seres humanos são poucos. Mas aqueles seres que quando falecem como seres
humanos renascem no inferno são mais numerosos. Por qual razão? Porque,
bhikkhus, eles não viram as Quatro Nobres Verdades. Quais quatro? A nobre
verdade do sofrimento, a nobre verdade da origem do sofrimento, a nobre verdade
da cessação do sofrimento, a nobre verdade do caminho que conduz à cessação do
sofrimento.<o:p></o:p></span></p>



<p class=Normal><span lang=PT-BR style='mso-ansi-language:PT-BR'>“Portanto,
bhikkhus, um esforço deve ser feito para compreender: ‘Isto é sofrimento.’ Um
esforço deve ser feito para compreender: ‘Esta é a origem do sofrimento.’ Um
esforço deve ser feito para compreender: ‘Esta é a cessação do sofrimento.’ Um
esforço deve ser feito para compreender: ‘Este é o caminho que conduz à
cessação do sofrimento.’” <o:p></o:p></span></p>



